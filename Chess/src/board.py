    def move_piece(self, move):
        # Extract initial and final positions from the move
        initial_square = self.squares[move.initial.row][move.initial.col]
        final_square = self.squares[move.final.row][move.final.col]
        piece = initial_square.piece

        # Handle capture
        if final_square.piece is not None and final_square.piece.color != piece.color:
            self.capture_piece(final_square)

        # Move piece to the new square
        final_square.piece = piece
        initial_square.piece = None  # Clear the initial square

        # Handle special moves
        if isinstance(piece, Pawn):
            # Pawn promotion
            if (piece.color == 'white' and move.final.row == 0) or (piece.color == 'black' and move.final.row == 7):
                final_square.piece = Queen(piece.color)  # Promote to Queen as an example

            # En passant (simplified; adjust according to your game logic)
            if move.final.col != move.initial.col and final_square.piece is None:
                # Assume the pawn moves diagonally without capture
                self.squares[move.initial.row][move.final.col].piece = None  # Remove the passed pawn

        elif isinstance(piece, King) and abs(move.final.col - move.initial.col) == 2:
            # Castling
            rook_col = 0 if move.final.col < move.initial.col else 7  # Assuming rook's initial position
            rook_final_col = (move.final.col - 1) if move.final.col < move.initial.col else (move.final.col + 1)
            rook_square = self.squares[move.initial.row][rook_col]
            rook_final_square = self.squares[move.final.row][rook_final_col]

            rook_final_square.piece = rook_square.piece  # Move the rook
            rook_square.piece = None  # Clear the rook's initial square

        # Update piece's moved status if applicable
        piece.moved = True

