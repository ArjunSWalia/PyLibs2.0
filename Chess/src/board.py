def evaluate_board(board):
    piece_value = {'Pawn': 10, 'Knight': 30, 'Bishop': 30, 'Rook': 50, 'Queen': 90, 'King': 900}
    board_value = 0
    for row in range(len(board.squares)):
        for col in range(len(board.squares[row])):
            square = board.squares[row][col]
            if square.piece:
                piece_val = piece_value[type(square.piece).__name__]
                position_val = 0  # Placeholder for positional values
                # Example positional adjustment: central control
                if (row in [3, 4] and col in [3, 4]):
                    position_val += 1  # Central squares get a small bonus
                total_val = piece_val + position_val
                if square.piece.color == 'white':
                    board_value += total_val
                else:
                    board_value -= total_val
    return board_value

def move_value(move, board):
    """Estimate the value of a move for prioritization."""
    # Basic value assignments
    piece_values = {'Pawn': 1, 'Knight': 3, 'Bishop': 3, 'Rook': 5, 'Queen': 9, 'King': 0}
    value = 0

    # Check for capture
    if not board.squares[move.final.row][move.final.col].isempty():
        captured_piece = board.squares[move.final.row][move.final.col].piece
        value += piece_values[type(captured_piece).__name__]

    # Prioritize pawn promotion (assuming promotion to Queen for simplicity)
    if isinstance(move.piece, Pawn) and (move.final.row == 0 or move.final.row == 7):
        value += piece_values['Queen']

    # Additional heuristics could be added here (e.g., checking, castling, etc.)

    return value


def order_moves(moves, board):
    """Order moves based on their estimated value."""
    moves_with_values = [(move, move_value(move, board)) for move in moves]
    # Sort moves by their value in descending order
    moves_with_values.sort(key=lambda x: x[1], reverse=True)
    # Return ordered moves without values
    ordered_moves = [mv[0] for mv in moves_with_values]
    return ordered_moves



def minimax_alpha_beta(board, depth, alpha, beta, maximizing_player):
    if depth == 0 or board.is_game_over():
        return evaluate_board(board), None

    if maximizing_player:
        max_eval = float('-inf')
        best_move = None
        for move in board.get_all_valid_moves('white'):  # Assuming white is AI
            board_copy = copy.deepcopy(board)
            board_copy.move_piece(move)
            eval, _ = minimax_alpha_beta(board_copy, depth - 1, alpha, beta, False)
            if eval > max_eval:
                max_eval = eval
                best_move = move
            alpha = max(alpha, eval)
            if beta <= alpha:
                break
        return max_eval, best_move
    else:
        min_eval = float('inf')
        best_move = None
        for move in board.get_all_valid_moves('black'):  # Assuming black is opponent
            board_copy = copy.deepcopy(board)
            board_copy.move_piece(move)
            eval, _ = minimax_alpha_beta(board_copy, depth - 1, alpha, beta, True)
            if eval < min_eval:
                min_eval = eval
                best_move = move
            beta = min(beta, eval)
            if beta <= alpha:
                break
        return min_eval, best_move

def ai_move(board, depth):
    _, move = minimax_alpha_beta(board, depth, float('-inf'), float('inf'), True)  # True if AI is maximizing; adjust accordingly
    if move:
        board.move_piece(move)  # Ensure this method updates the board with the AI's move
        return True
